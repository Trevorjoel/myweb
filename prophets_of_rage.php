   <p> <em>Make America RAGE again</em></p><p>
Very excited to hear members of Rage against the Machine (RATM), Public Enemy and Cypress Hill have joined forces to make the supergroup, Prophets of Rage.</p><p>

As one would expect, the group is politically charged and fired up about the current political environment. <br>
In the group's promo video Tom Morello (former Rage Against the Machine guitarist) stated;<br>
"Justice needs a fist in the face, and in America's greatest hour of need, Prophets of Rage have come together".<br>
Tom went on to say, <br>"We're going to demonstrate what it really means to be anti-establishment, we're going to demonstrate what it really means to rage against the machine". <br>Sounds great to me, what about the music?</p><p>

Tom Morello, long time hero of mine, has not lost the chops on guitar. <br>Blazing out classic RATM  riffs  backed up by RATM's bassist Tim Commerford and RATM drummer Brad Wilk. <br>

With Chuck D (public enemy), B-Real (Cypress Hill),  layin' down vocals over such a tight unit and beats by  DJ Lord this is one group to get excited about.</p><p>

Tuesday 31st of May, 2016 Prophets of rage did their 1st live debut at L.A.'s Whisky a Go Go. <br>Read more about that explosive gig here: <br><br><a href="http://www.rollingstone.com/music/news/prophets-of-rage-set-defiant-tone-at-explosive-l-a-debut-20160601?page=2">Rolling stone magazine</a>
</p><p>
<img style="float:right;" class="tst3"src="images/por_play.jpg" alt="erlbanner" >
I for one am anticipating developments both onstage and offstage.<br>
"That's why we're back, to kick the establishment in the fucking mouth," said Tim Commerford.<br>
When asked what their message is band members responded simply, <br>
"To make America rage again".<br><br>
Sounds good to me.</p><p>
   
