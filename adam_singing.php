			- The way the voice anatomically functions (for a better
			understanding of the singing process).<br>
			<br> - Musical theory (notes, keys, rhythm, tempo, dynamics,
			registers).<br>
			<br> - Tips and tricks (riffs and runs, melodies, how to avoid strain
			and pain, tone colors).<br>
			<br> - Exercises (simple, intermediate and advanced, to make rapid
			increases in range, to sing more stable, in key and how to bridge in
			between the registers).<br>
			<br> - Materials to help you sing better and inspire you to keep up
			the good work.<br>
