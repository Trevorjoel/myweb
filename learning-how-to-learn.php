	<h1 style="font-size: 250%">Learning how to learn</h1>
	<article class="articleform">




		<img class="tst3" src="images/573647.jpg" alt="php" width="288"
			height="180">
		<p>
			<b>The internet is a wonderful place for learning about anything,</b>
			however, the hardest part is finding good information, the needles in
			the haystack so to speak. <br><br>You can spend hours on terrible tutorials
			with teachers who have the charm and personality of a wet ashtray or
			days scouring through poorly put together instructions.
		</p>

		<p>I have been a teacher for a few years now and believe I know a
			thing or two about how to teach and learn. Here are the main problems
			with learning and how to overcome them.</p>

		<p align="center">
			<b>Too hard/too easy.</b>
		</p>
		<p>Learning something is an incremental process, you don't learn to
			drive by entering a Formula 1 race. <br><br>Find your level, if in doubt
			revert to the basics and build a solid foundation.</p>

		<p align="center">
			<b>Patience/attitude/giving up.</b>
		</p>
		<p>
			There are no quick fixes, those products for penis enlargement don't
			work. <br><br>You wanna learn something, you gotta' wanna'
			learn it. <br><br>NEVER say "I'm not smart enough" either way you think,
			you're right. <br><br>NEVER let yourself get too frustrated, you just need
			the right info at the right time, take a quick break come back at it from ad
			different point of view. <br><br>If it fails go
			over what you have already learnt.<br> <br>NEVER give up.
		</p>
		<p></p>

		<b><p align="center">Hard work/organisation.</p></b>

		<p>
			As above and furthermore, there is no substitute for time, effort,
			and attitude.<br> <br>It's better to do a little every day than to binge
			learn for hours, and have long periods without practice. <br><br>Each
			person's brain can only concentrate for a given period of time, the
			good news is this can be extended with practice. <br><br>Ensure you set
			goals and schedule undisturbed time for learning (this means no
			Facebook, granny porn, and the like.) A good technique is to use
			a timer, 20 mins of study then take a reward break for five mins and repeat.
		</p>

		<p align="center">
			<b>Practical application of theory.</b>
		</p>
		<p>If you can't practice it for yourself in a real world situation it
			won't stick. <br><br>Begin a project and apply your knowledge as you learn
			it. <br><br>You never know, your inexperience and the new perspective on the
			subject could be pure gold. <br><br>Many great breakthroughs in physics have
			been discovered by undergraduates. Just try!</p>

		<p align="center">
			<b>Poor information and presentation of learning materials.</b>
		</p>
		<p>If you don't like the teacher it's hard to learn from them, a bit
			of enthusiasm and humour goes a long way to getting the information
			to stick with the students. Especially don't pay your hard earned money for a poor teacher.</p>

		<p>So with this in mind, each new article should have the basic
			information:</p>
		<p></p>
		<p>
			<b>Title:</b>
		</p>
		<p>

			<b>Level:</b> Beginner, Pre-intermediate, Intermediate,
			Upper-intermediate and Advanced.
		</p>
		<p>
			<b>Description:</b> Some information on the materials i.e why it has
			been chosen and who it is for.
		</p>
		<p>
			<b>Format:</b> Is it video, pictorial, tutorial, course, article,
			scientific paper.
		</p>
		<p>
			<b>Prior learning:</b> What you should have already learnt.
		</p>
		<p>So on this page we share with you the nuggets of informational gold
			we have found while searching through the giant haystack that is the
			interwebs.</p>
		<em><p>Please don't be afraid to send in your useful nuggets.</p></em>
		<p>So now that's out of the way let's get learning.</p>
