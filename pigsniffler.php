"Yeh, how so," replied Joe, obliging the invitation to hear what's on his mind.
"Well y' know, since we been feedin' them pigs that special grain they make us feed 'em they been actin' all funny," said Cleetus, before he tipped up his can high and chugged back a large portion of his Beer.
