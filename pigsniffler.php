Joe lifted his  John Deere cap and scratched his head with the brim. "Hell your Pa's gettin' long in the tooth, ya' don't think he's goin' a bit crazy?"
Cleetus's shot back, "Naw', the old coot's pushin' 90 an' sharp as a tack." 
The pair paused in quiet reflection drinking beer and looking out from the front porch over the dusty evening horizon. Soon in the distance, the two old pals noticed a pickup truck billowing dust behind it as it sped up the long driveway. </p> 





